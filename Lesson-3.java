import java.util.*;

public class Main {
    public static void main(String[] args) {
      
      int a = 50;
      int b = 8;
      
      int result1 = a + b;
      System.out.println(result1);
      
      int result2 = a * b;
      System.out.println(result2);
      
      int result3 = a - b;
      System.out.println(result3);
      
      // Целочисленное деление
      int result4 = a / b;
      System.out.println(result4);
      
      // Получение остатка
      int result5 = a % b;
      System.out.println(result5);
  }
}

